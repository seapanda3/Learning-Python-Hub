# in
# Returns True if the specified value is found in the sequence.
# In a list
fruits = ['apple', 'banana', 'cherry']
print('apple' in fruits)  # True
print('orange' in fruits)  # False

# In a string
name = "Python"
print('P' in name)  # True
print('z' in name)  # False


# not in
# Returns True if the specified value is not found in the sequence
# In a list
numbers = [1, 2, 3, 4]
print(5 not in numbers)  # True
print(3 not in numbers)  # False

# In a string
text = "Hello World"
print('x' not in text)  # True
print('o' not in text)  # False
