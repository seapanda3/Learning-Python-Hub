# The range function generates numbers within a range

for i in range(5): # Generates 0, 1, 2, 3, 4
    print(i)
    
    
# Range Variants

# range(start, stop)
for i in range(2, 6): # 2 to 5
    print(i)
    
    
# range(start, stop, step)
for i in range(1, 10, 2): # 1, 3, 5, 7, 9
    print(i)
    
    
